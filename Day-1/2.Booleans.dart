void main() {
  // Boolean---: bool , their literals are: true and false (Both are compiled-time constants).
  bool b = true;
  print(b);

  // Checking for an empty string
  var name = '';
  print(name.isEmpty);

  // Checking for zero
  var x = 0;
  print(x == 0);

  // Checking for null
  var fruit = null;
  print(fruit == null);

  // Checking for NaN
  var y = 5 / 0;
  print(y.isNaN);
  
}
